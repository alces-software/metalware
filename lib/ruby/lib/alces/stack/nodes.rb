module Alces
  module Stack
    class Nodes
      def initialize(gender, &block)
        @gender = gender
        raise "Could not find gender group" if `nodeattr -c #{@gender}`.empty?
        yield self if !block.nil?
      end
      def each(&block)
        node_list = `nodeattr -c #{@gender}`.split(',').each(&block)
      end
    end
  end
end